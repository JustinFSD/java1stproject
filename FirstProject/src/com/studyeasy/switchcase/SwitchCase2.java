package com.studyeasy.switchcase;

public class SwitchCase2 {
	public static void main(String[] args) {
		String student = "Justin";
		
	switch(student) {
	case "Sam":
		System.out.println(student + " is from Bangalore");
		break;
	case "Justin":
		System.out.println(student + " is from Chennai");
		break;
	case "Raj":
		System.out.println(student + " is from Delhi");
		}
	}

}
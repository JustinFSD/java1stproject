package com.studyeasy.releationaloperator;

public class ReleationalOper {
	public static void main(String[] args) {
		int x=593;
		int y=690;
		
		System.out.println(x==y);
		System.out.println(x>=y);
		System.out.println(x<=y);
		System.out.println(x!=0);
		System.out.println(x>10);
		System.out.println(y<0);
	}

}